# NOTE o join é um comando que pega os valores de uma lista e junta em unica variavel

# lista de dias da semana
dias =[
    "Domingo",
    "Segunda-Feira",
    "Terça-Feira",
    "Quarta-Feira",
    "Quinta-Feira",
    "Sexta-Feira",
    "Sabado"
]

separador = "," # delimitador
semana = separador. join(dias) 

# exibi na tela
print(semana)
