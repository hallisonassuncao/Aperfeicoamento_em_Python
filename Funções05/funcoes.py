#declaração de funções
def calcular_quadrilatero(base, altura):
    area = base * altura
    return area

def calcular_triangulo(base, altura):
    area = (base * altura) / 2
    return area

def calcular_trapezio(base_maior, base_menor, altura):
    area = ((base_maior + base_menor) * altura) / 2
    return area

